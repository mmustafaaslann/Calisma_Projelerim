package ders04_Increment_Decrement;

public class C04_İncrement_Decrement {
    public static void main(String[] args) {
        int sayı = 10 ;
        // bu sayıyı 3 artılarım ve yazdıralım

        sayı = sayı+3;
        System.out.println("sayı = " + sayı);

        int s = 12 ;
        //bu sayıyı 1 artıralım
        s++;//önce işlem sonra artır
        System.out.println("s = " + s);

    }
}
